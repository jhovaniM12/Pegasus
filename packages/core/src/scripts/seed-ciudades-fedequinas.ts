async function main(): Promise<void> {
  const { getDataSource } = await import("../database/data-source.js");
  const dataSource = await getDataSource();

  try {
    const cityRepo = dataSource.getRepository(City);

    for (let offset = 0; offset < FEDEQUINAS_CIUDADES.length; offset += BATCH_SIZE) {
      const batch = FEDEQUINAS_CIUDADES.slice(offset, offset + BATCH_SIZE);

      await cityRepo.upsert(
        batch.map((ciudad) => ({
          externalId: ciudad.externalId,
          sourceSystem: SOURCE_SYSTEM,
          departmentCode: ciudad.departmentCode,
          name: ciudad.name
        })),
        {
          conflictPaths: ["externalId", "sourceSystem"],
          skipUpdateIfNoValuesChanged: true
        }
      );
    }

    const loadedCount = await cityRepo.count({
      where: { sourceSystem: SOURCE_SYSTEM }
    });

    console.log(`Ciudades Fedequinas cargadas: ${loadedCount} de ${FEDEQUINAS_CIUDADES.length}.`);
  } finally {
    await dataSource.destroy();
  }
}

main().catch((error: unknown) => {
  console.error("Error al cargar ciudades Fedequinas:");
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
